//Program of multiplication table(13) using for loop.

public class prog14 {

    public static void main(String[] args) {

        int num = 13;
        for(int i = 1; i <= 10; ++i)
        {
            System.out.printf("%d * %d = %d \n", num, i, num * i);
        }
    }
}