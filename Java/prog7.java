//Program of switch case with operators.

public class prog7 {

	public static void main(String[] args) {
        int a=5;
        int b=3;
        int c;
        int roll=4;
		switch(roll) {
		case 1:
            c=a+b;
			System.out.println("Add");
			break;
		
		case 2:
            c=a-b;
			System.out.println("Substract");
			break;
	
		case 3:
            c=a*b;
			System.out.println("Multiply");
			break;

		case 4:
            c=a/b;
			System.out.println("Divide");
			break;
			
				default:
			}
	}

}
